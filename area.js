function areaQuadrado(base, altura) {
    return quadrado = base * altura;

}
module.exports.areaQuadrado = areaQuadrado;

function areaTriangulo(base, altura) {
    return triangulo = base * altura / 2;

}
module.exports.areaTriangulo = areaTriangulo;

function areaCirculo(pi,raio) {
    return criculo = pi * (raio * raio);

}
module.exports.areaCirculo = areaCirculo;